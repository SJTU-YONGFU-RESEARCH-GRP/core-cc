module golay_ecc_w16 (
    input  wire clk,
    input  wire rst_n,
    input  wire encode_en,
    input  wire decode_en,
    input  wire [15:0] data_in,
    input  wire [45:0] codeword_in,
    output reg  [45:0] codeword_out,
    output reg  [15:0] data_out,
    output reg  error_detected,
    output reg  error_corrected,
    output reg  valid_out
);

    // Instantiate Blocks
    wire [22:0] cw_out_0;
    wire [7:0] d_out_0;
    wire err_det_0, err_corr_0;
    golay_23_12_block u_blk_0 (
        .data_in(data_in[7:0]),
        .codeword_in(codeword_in[22:0]),
        .codeword_out(cw_out_0),
        .data_out(d_out_0),
        .error_detected(err_det_0),
        .error_corrected(err_corr_0)
    );
    wire [22:0] cw_out_1;
    wire [7:0] d_out_1;
    wire err_det_1, err_corr_1;
    golay_23_12_block u_blk_1 (
        .data_in(data_in[15:8]),
        .codeword_in(codeword_in[45:23]),
        .codeword_out(cw_out_1),
        .data_out(d_out_1),
        .error_detected(err_det_1),
        .error_corrected(err_corr_1)
    );

    // Aggregation
    assign error_detected = err_det_0 | err_det_1;
    assign error_corrected = err_corr_0 | err_corr_1;
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            codeword_out <= 0;
            data_out <= 0;
            valid_out <= 0;
        end else if (encode_en) begin
            codeword_out[22:0] <= cw_out_0;
            codeword_out[45:23] <= cw_out_1;
            valid_out <= 1;
        end else if (decode_en) begin
            data_out[7:0] <= d_out_0[7:0];
            data_out[15:8] <= d_out_1[7:0];
            valid_out <= 1;
        end else begin
            valid_out <= 0;
        end
    end
endmodule