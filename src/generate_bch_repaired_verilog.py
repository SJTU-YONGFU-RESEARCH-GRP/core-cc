#!/usr/bin/env python3
"""
Emit repaired BCH decoders for CORE-CC.

- w4, w8: syndrome -> err_mask case tables (t=1)
- w16: full ROM from bch_codec.build_syndrome_mask_table (t=3, BCH(31,16))
- w32: use src/generate_bch_decoder_fsm.py (BM+Chien FSM, k_encode=36, parity=27)
"""

from __future__ import annotations

import os
import re
import sys

sys.path.insert(0, os.path.dirname(__file__))
from bch_codec import BCHCodec, build_syndrome_mask_table, BCH_CONFIGS  # noqa: E402


def read_raw_module(width: int) -> str:
    path = os.path.join(
        os.path.dirname(__file__), "..", "verilogs", "raw_llm_generated", "generated",
        f"bch_ecc_w{width}.v",
    )
    with open(path, encoding="utf-8") as f:
        return f.read()


def extract_encoder_block(raw: str) -> str:
    """Keep data_k, parity assigns from generated raw file."""
    lines = []
    for line in raw.splitlines():
        if line.strip().startswith("wire [") and "data_k" in line:
            lines.append(line)
        elif line.strip().startswith("wire [") and "parity" in line:
            lines.append(line)
        elif line.strip().startswith("assign parity"):
            lines.append(line)
    return "\n".join(lines)


def extract_syndrome_wires(raw: str) -> str:
    lines = [ln for ln in raw.splitlines() if ln.strip().startswith("wire synd_")]
    return "\n".join(lines)


def emit_case_table(width: int, table: dict) -> str:
    codec = BCHCodec.for_width(width)
    m = codec.gf.m
    n_synd = 2 * codec.t
    key_bits = n_synd * m

    lines = [
        f"    wire [{key_bits - 1}:0] syndrome_key;",
        "    assign syndrome_key = {",
    ]
    chunks = []
    for j in range(1, n_synd + 1):
        for b in range(m):
            chunks.append(f"synd_{j}_{b}")
    lines.append("        " + ", ".join(reversed(chunks)))
    lines.append("    };")
    lines.append("")
    lines.append(f"    reg [{codec.n - 1}:0] err_mask;")
    lines.append("    always @(*) begin")
    lines.append("        case (syndrome_key)")

    for key in sorted(table.keys()):
        mask = table[key]
        lines.append(f"            {key_bits}'d{key}: err_mask = {codec.n}'b{mask:0{codec.n}b};")
    lines.append(f"            default: err_mask = {codec.n}'b0;")
    lines.append("        endcase")
    lines.append("    end")
    return "\n".join(lines)


def generate_repaired(width: int, out_dir: str) -> None:
    raw = read_raw_module(width)
    codec = BCHCodec.for_width(width)
    n, k, t = codec.n, codec.k, codec.t
    table = build_syndrome_mask_table(width)

    enc = extract_encoder_block(raw)
    synd = extract_syndrome_wires(raw)
    case_blk = emit_case_table(width, table)

    mod = f"bch_ecc_w{width}_lut" if width == 16 else f"bch_ecc_w{width}"
    body = f"""// Repaired {mod} - BCH({n},{k},t={t}) syndrome ROM (LUT / truth-table golden)
// Auto-generated by src/generate_bch_repaired_verilog.py
module {mod} (
    input  wire clk,
    input  wire rst_n,
    input  wire encode_en,
    input  wire decode_en,
    input  wire [{width - 1}:0] data_in,
    input  wire [{n - 1}:0] codeword_in,
    output reg  [{n - 1}:0] codeword_out,
    output reg  [{width - 1}:0] data_out,
    output reg  error_detected,
    output reg  error_corrected,
    output reg  valid_out
);
    // Configuration: n={n}, k={k}, t={t}
{enc}

{synd}

    wire any_syndrome = |syndrome_key;

{case_blk}

    wire [{n - 1}:0] corrected_cw = codeword_in ^ err_mask;

    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            codeword_out    <= {n}'b0;
            data_out        <= {width}'b0;
            error_detected  <= 1'b0;
            error_corrected <= 1'b0;
            valid_out       <= 1'b0;
        end else if (encode_en) begin
            codeword_out    <= {{data_k, parity}};
            data_out        <= {width}'b0;
            error_detected  <= 1'b0;
            error_corrected <= 1'b0;
            valid_out       <= 1'b1;
        end else if (decode_en) begin
            codeword_out    <= {n}'b0;
            data_out        <= corrected_cw[{codec.parity_bits + width - 1}:{codec.parity_bits}];
            error_detected  <= any_syndrome;
            error_corrected <= any_syndrome;
            valid_out       <= 1'b1;
        end else begin
            valid_out       <= 1'b0;
        end
    end

endmodule
"""

    os.makedirs(out_dir, exist_ok=True)
    fname = f"bch_ecc_w{width}_lut.v" if width == 16 else f"bch_ecc_w{width}.v"
    path = os.path.join(out_dir, fname)
    with open(path, "w", encoding="utf-8") as f:
        f.write(body)
    print(f"Wrote {path} ({len(table)} syndrome entries)")


def main() -> None:
    out = os.path.join(os.path.dirname(__file__), "..", "verilogs", "repaired_golden", "generated")
    gen = os.path.join(os.path.dirname(__file__), "..", "verilogs", "generated")
    for width in (4, 8, 16):
        generate_repaired(width, out)
        generate_repaired(width, gen)


if __name__ == "__main__":
    main()
